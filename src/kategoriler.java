    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Boolean durum = true;
        String kutu = "";
        for (Component i : jPanel2.getComponents()) {

            if (i instanceof JTextField) {
                JTextField jt = (JTextField) i;
                if (jt.getText().equals("")) {

                    kutu += " " + jt.getName();
                    durum = false;

                }
            }

        }
        if (durum) {
            String adi = jTextField2.getText();
            String no = jTextField3.getText();
            String adres = jTextField4.getText();
            int id = Integer.parseInt(jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString());
            DB db = new DB();
            try {

                db.baglan().executeQuery("call fncUpdateKategori('" + adi + "','" + no + "','" + adres + "'," + id + ")");

            } catch (Exception e) {
            }
            tabloDoldur();
        }else if(kutu.split(" ").length>1){

            JOptionPane.showMessageDialog(this, "Lütfen" + kutu + " alanlarını doldurunuz");
            System.out.println(kutu); 
       }else{
            
            JOptionPane.showMessageDialog(this, "Lütfen" + kutu + " alanını doldurunuz");
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

